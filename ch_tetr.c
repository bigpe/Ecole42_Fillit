#include "fillit.h"

int	ch_tetr(char **str, int j, int i)
{
	int	res;

	res = 0;
	if (i == 4 && j == 3 )
		return (0);
	if (str[j][i] == '#')
	{
		if ((i > 0 && j >= 0) && str[j][i - 1] == '#')
			res++;
		if ((i < 3 && j < 4) && str[j][i + 1] == '#')
			res++;
		if ((i >= 0 && j > 0) && str[j - 1][i] == '#')
			res++;
		if ((i <= 4 && j < 3) && str[j + 1][i] == '#')
			res++;
	}
	i++;
	if (i == 4 && j < 3)
	{
		i = 0;
		j++;
	}
	return (res + ch_tetr(str, j, i));
}
